CREATE TABLE IF NOT EXISTS routines (
    CREATED TIMESTAMP,
    DATA_TYPE VARCHAR,
    IS_DETERMINISTIC VARCHAR,
    IS_IMPLICITLY_INVOCABLE VARCHAR,
    IS_NULL_CALL VARCHAR,
    IS_USER_DEFINED_CAST VARCHAR,
    LAST_ALTERED TIMESTAMP,
    MAX_DYNAMIC_RESULT_SETS SMALLINT,
    NUMERIC_PRECISION SMALLINT,
    NUMERIC_PRECISION_RADIX SMALLINT,
    NUMERIC_SCALE INTEGER,
    ROUTINE_BODY VARCHAR,
    ROUTINE_CATALOG VARCHAR,
    ROUTINE_DEFINITION VARCHAR,
    ROUTINE_NAME VARCHAR,
    ROUTINE_SCHEMA VARCHAR,
    ROUTINE_TYPE VARCHAR,
    SCHEMA_LEVEL_ROUTINE VARCHAR,
    SPECIFIC_CATALOG VARCHAR,
    SPECIFIC_NAME VARCHAR,
    SPECIFIC_SCHEMA VARCHAR,
    SQL_DATA_ACCESS VARCHAR
);
